// Task 2: Guess the output
console.log(typeof 10);        // "number"
console.log(typeof "10");      // "string"
console.log(typeof true);      // "boolean"
console.log(typeof null);      // "object" (JS quirk)
console.log(typeof undefined); // "undefined"

